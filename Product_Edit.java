    private void Button_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_editActionPerformed
        try {
            if(TextField_id.toString().equals(""))
                TextField_id.setText(username);
            if(TextField_name.toString().equals(""))
                TextField_name.setText(name);
            if(TextField_unit.toString().equals(""))
                TextField_unit.setText(unit);
            if(TextField_price.toString().equals(""))
                TextField_price.setText(price);
            if(TextField_type.toString().equals(""))
                TextField_type.setText(type);
            
            MongoClient mongo = new MongoClient("localhost", 27017);

            DB db = mongo.getDB("inthanin");

            DBCollection coll = db.getCollection("MS_Product");

            BasicDBObject newDocument = new BasicDBObject();

            DBObject query = new BasicDBObject().append("MS_Product_Id", username);
            System.out.println(newusername);
            newDocument.append("$set", new BasicDBObject().append("MS_Product_Id", newusername));

            coll.update(query, newDocument);

            DBObject query2 = new BasicDBObject().append("MS_Product_Name", name);
            System.out.println(newname);
            newDocument.append("$set", new BasicDBObject().append("MS_Product_Name", newname));

            coll.update(query2, newDocument);

            DBObject query3 = new BasicDBObject().append("MS_Product_Unit", unit);
            System.out.println(newunit);
            newDocument.append("$set", new BasicDBObject().append("MS_Product_Unit", newunit));

            coll.update(query3, newDocument);

            DBObject query4 = new BasicDBObject().append("MS_Product_Price", price);
            System.out.println(newprice);
            newDocument.append("$set", new BasicDBObject().append("MS_Product_Price", newprice));

            coll.update(query4, newDocument);

            DBObject query5 = new BasicDBObject().append("MS_Product_Type", type);
            System.out.println(newtype);
            newDocument.append("$set", new BasicDBObject().append("MS_Product_Type", newtype));

            coll.update(query5, newDocument);
            JOptionPane.showMessageDialog(this, "Update now.");

        } catch (Exception e) {

        }
        this.setVisible(false);
    }//GEN-LAST:event_Button_editActionPerformed

