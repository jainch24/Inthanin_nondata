    public void getpid(String pid) {
        username = pid;
        try {
            MongoClient mongo = new MongoClient("localhost", 27017);
            DB db = mongo.getDB("inthanin");
            DBCollection table = db.getCollection("MS_Product");
            BasicDBObject document = new BasicDBObject();
            BasicDBObject searchQuery = new BasicDBObject();

            searchQuery.put("MS_Product_Id", username);
            DBCursor cursor = table.find(searchQuery);
            if (cursor.hasNext() == true) {
                for (DBObject a : cursor) {
                    System.out.println(cursor.next());
                    name = (String) a.get("MS_Product_Name");
                    unit = (String) a.get("MS_Product_Unit");
                    price = (String) a.get("MS_Product_Price");
                    type = (String) a.get("MS_Product_Type");
                    System.out.println(username+ name + unit + price + type);
                    TextField_id.setText(username);
                    TextField_name.setText(name);
                    TextField_unit.setText(unit);
                    TextField_price.setText(price);
                    TextField_type.setText(type);

                }
            } else {
                System.out.println("Please select id");
                JOptionPane.showMessageDialog(this, "Please select Id column.");
                this.setVisible(false);
            }

        } catch (Exception e) {
        }

    }

